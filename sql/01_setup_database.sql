-- Entity Vault: Database and Schema Setup
-- Run this script first to create all database objects.

-- =============================================================================
-- DATABASE
-- =============================================================================
CREATE OR REPLACE DATABASE ENTITY_VAULT_DB
    COMMENT = 'Entity Vault - Identity Resolution, Encoding, and Enrichment Platform';

-- =============================================================================
-- SCHEMAS
-- =============================================================================
CREATE SCHEMA ENTITY_VAULT_DB.CORE
    COMMENT = 'Golden record entities, identifiers, and type schemas';

CREATE SCHEMA ENTITY_VAULT_DB.RESOLUTION
    COMMENT = 'AI-powered resolution: embeddings, search service, resolution procedures';

CREATE SCHEMA ENTITY_VAULT_DB.ENCODING
    COMMENT = 'Namespaces, HMAC key management, and encoded ID mappings';

CREATE SCHEMA ENTITY_VAULT_DB.ENRICHMENT
    COMMENT = 'Entity groups, contributions, and marketplace catalog';

CREATE SCHEMA ENTITY_VAULT_DB.POLICY
    COMMENT = 'Consent records, suppression records, and transcode policies';

CREATE SCHEMA ENTITY_VAULT_DB.AUDIT
    COMMENT = 'Immutable audit trail for all vault operations';

-- =============================================================================
-- CORE TABLES
-- =============================================================================
CREATE TABLE ENTITY_VAULT_DB.CORE.ENTITY_TYPE_SCHEMAS (
    SCHEMA_ID VARCHAR(36) DEFAULT UUID_STRING() NOT NULL,
    CLASS_NAME VARCHAR(100) NOT NULL,
    CLASS_LABEL VARCHAR(200),
    CLASS_DESCRIPTION VARCHAR(1000),
    PARENT_NAME VARCHAR(100),
    ATTRIBUTES VARIANT,
    RESOLUTION_CONFIG VARIANT,
    CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT PK_ENTITY_TYPE_SCHEMAS PRIMARY KEY (SCHEMA_ID),
    CONSTRAINT UQ_CLASS_NAME UNIQUE (CLASS_NAME)
);

CREATE TABLE ENTITY_VAULT_DB.CORE.ENTITIES (
    ENTITY_ID VARCHAR(36) DEFAULT UUID_STRING() NOT NULL,
    ENTITY_TYPE VARCHAR(50) NOT NULL,
    LIFECYCLE_STATE VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    TRUST_TIER VARCHAR(20) DEFAULT 'STANDARD',
    METADATA VARIANT DEFAULT PARSE_JSON('{}'),
    CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    DELETED_AT TIMESTAMP_LTZ,
    CONSTRAINT PK_ENTITIES PRIMARY KEY (ENTITY_ID)
);

CREATE TABLE ENTITY_VAULT_DB.CORE.IDENTIFIERS (
    IDENTIFIER_ID VARCHAR(36) DEFAULT UUID_STRING() NOT NULL,
    ENTITY_ID VARCHAR(36) NOT NULL,
    IDENTIFIER_TYPE VARCHAR(50) NOT NULL,
    IDENTIFIER_VALUE VARCHAR(500),
    NORMALIZED_VALUE_HASH VARCHAR(128) NOT NULL,
    SOURCE_SYSTEM VARCHAR(100),
    CONFIDENCE_SCORE FLOAT DEFAULT 1.0,
    LINK_TYPE VARCHAR(30) DEFAULT 'DETERMINISTIC',
    FIRST_SEEN_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    LAST_SEEN_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    METADATA VARIANT DEFAULT PARSE_JSON('{}'),
    CONSTRAINT PK_IDENTIFIERS PRIMARY KEY (IDENTIFIER_ID),
    CONSTRAINT FK_IDENTIFIERS_ENTITY FOREIGN KEY (ENTITY_ID) REFERENCES ENTITY_VAULT_DB.CORE.ENTITIES(ENTITY_ID)
);

ALTER TABLE ENTITY_VAULT_DB.CORE.IDENTIFIERS CLUSTER BY (NORMALIZED_VALUE_HASH);

