CREATE OR REPLACE VIEW ENTITY_VAULT_DB.RESOLUTION.ENTITIES_SEARCHABLE AS
SELECT
    e.ENTITY_ID,
    e.ENTITY_TYPE,
    CONCAT_WS(' | ',
        CASE WHEN e.METADATA:i_brand::VARCHAR IS NOT NULL THEN 'brand: ' || e.METADATA:i_brand::VARCHAR END,
        CASE WHEN e.METADATA:i_category::VARCHAR IS NOT NULL THEN 'category: ' || e.METADATA:i_category::VARCHAR END,
        CASE WHEN e.METADATA:i_class::VARCHAR IS NOT NULL THEN 'class: ' || e.METADATA:i_class::VARCHAR END,
        CASE WHEN e.METADATA:i_manufact::VARCHAR IS NOT NULL THEN 'manufacturer: ' || e.METADATA:i_manufact::VARCHAR END,
        CASE WHEN e.METADATA:i_product_name::VARCHAR IS NOT NULL THEN 'product: ' || e.METADATA:i_product_name::VARCHAR END,
        CASE WHEN e.METADATA:i_color::VARCHAR IS NOT NULL THEN 'color: ' || e.METADATA:i_color::VARCHAR END,
        CASE WHEN e.METADATA:i_size::VARCHAR IS NOT NULL THEN 'size: ' || e.METADATA:i_size::VARCHAR END
    ) AS SEARCH_TEXT
FROM ENTITY_VAULT_DB.CORE.ENTITIES e
WHERE e.LIFECYCLE_STATE = 'ACTIVE';

CREATE OR REPLACE VIEW ENTITY_VAULT_DB.ENRICHMENT.VW_ENTITY_ALL_ATTRIBUTES AS
SELECT
    e.ENTITY_ID,
    'PLATFORM_GOLDEN_RECORD' AS SCHEMA_NAME,
    NULL AS NAMESPACE_ID,
    e.METADATA AS ATTRIBUTES,
    e.CREATED_AT AS CONTRIBUTED_AT
FROM ENTITY_VAULT_DB.CORE.ENTITIES e
WHERE e.LIFECYCLE_STATE = 'ACTIVE'
UNION ALL
SELECT
    ec.ENTITY_ID,
    cs.SCHEMA_NAME,
    ec.NAMESPACE_ID,
    ec.ATTRIBUTES,
    ec.CONTRIBUTED_AT
FROM ENTITY_VAULT_DB.ENRICHMENT.ENTITY_CONTRIBUTIONS ec
JOIN ENTITY_VAULT_DB.ENRICHMENT.CONTRIBUTION_SCHEMAS cs ON ec.SCHEMA_ID = cs.SCHEMA_ID
QUALIFY ROW_NUMBER() OVER (PARTITION BY ec.ENTITY_ID, ec.SCHEMA_ID ORDER BY ec.CONTRIBUTED_AT DESC) = 1;
